package shoppingCart;

public class Shoe extends Product {
	
	private String material;
	private int size;
	
	public String getMaterial() {
		return material;
	}
	
	public void setMaterial(String material) {
		this.material = material;
	} 
	
	public int getSize() {
		return size;
	}
	
	public void setSize(int size) {
		this.size = size;
	}	
}
